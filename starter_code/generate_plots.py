def plot_duration_lesson(runs):
    """
    Compare 90-second tests against 180-second final runs.

    Goal:
        Show that short tests can be misleading.

    In this report:
        - Text track scores lower at 180s because tail behavior emerges.
        - Multimodal track scores higher at 180s because steady-state improves.
    """

    # Text track:
    #   90s snapshot is compiled-70u.
    #   180s final is FINAL.
    text_90s = score(runs["compiled-70u"]) / 1e6
    text_180s = score(runs["FINAL"]) / 1e6

    # Multimodal track:
    #   90s snapshot is m-compiled-50u.
    #   180s final is FINAL-mixed.
    mm_90s = score(runs["m-compiled-50u"]) / 1e6
    mm_180s = score(runs["FINAL-mixed"]) / 1e6

    fig, ax = plt.subplots(figsize=(11, 6.5))

    # Two groups:
    #   index 0 = text
    #   index 1 = multimodal
    x = np.arange(2)

    # Width of each bar.
    w = 0.35

    # Left bar in each group: 90-second result.
    bars1 = ax.bar(
        x - w / 2,
        [text_90s, mm_90s],
        w,
        label="90 second test",
        color="#9ecae1",
    )

    # Right bar in each group: 180-second final.
    bars2 = ax.bar(
        x + w / 2,
        [text_180s, mm_180s],
        w,
        label="180 second test (FINAL)",
        color="#3182bd",
    )

    ax.set_xticks(x)
    ax.set_xticklabels(
        [
            "Text Track\n(compiled-70u → FINAL_text_70u)",
            "Multimodal Track\n(m-compiled-50u → FINAL-mixed)",
        ],
        fontsize=11,
    )

    ax.set_ylabel("Score (millions)", fontsize=12)
    ax.set_title(
        "The Duration Lesson: Short Tests Can Mislead — BOTH Ways\n"
        "Text 180s scored LOWER (tail emerged); "
        "Multimodal 180s scored HIGHER (steady-state was better).",
        fontsize=12.5,
        pad=14,
    )

    ax.grid(True, axis="y", alpha=0.25, ls="--")
    ax.legend(loc="upper right", fontsize=10.5)

    # Label 90-second bars.
    for bar, val in zip(bars1, [text_90s, mm_90s]):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            val + 1.5,
            f"{val:.1f}M",
            ha="center",
            fontsize=10.5,
            fontweight="bold",
        )

    # Label 180-second bars.
    for bar, val in zip(bars2, [text_180s, mm_180s]):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            val + 1.5,
            f"{val:.1f}M",
            ha="center",
            fontsize=10.5,
            fontweight="bold",
        )

    # Delta annotation for text track.
    ax.annotate(
        f"Δ {(text_180s / text_90s - 1) * 100:.0f}%",
        xy=(x[0], max(text_90s, text_180s)),
        xytext=(x[0], max(text_90s, text_180s) + 10),
        ha="center",
        fontsize=11,
        color="darkred",
        fontweight="bold",
    )

    # Delta annotation for multimodal track.
    ax.annotate(
        f"Δ +{(mm_180s / mm_90s - 1) * 100:.0f}%",
        xy=(x[1], max(mm_90s, mm_180s)),
        xytext=(x[1], max(mm_90s, mm_180s) + 10),
        ha="center",
        fontsize=11,
        color="darkgreen",
        fontweight="bold",
    )

    fig.tight_layout()
    out = PLOTS / "05_duration_lesson.png"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)

    print(f"Wrote {out}")


