def plot_final_summary(runs):
    """
    Compare final submission scores against workshop reference baselines.

    This plot includes:
        - FINAL-mixed submission
        - FINAL text submission
        - 2-replica mixed reference baseline
        - 4-replica mixed reference baseline
    """

    # Each tuple contains:
    #   display label,
    #   run key, or None for manually computed references,
    #   bar color.
    items = [
        ("FINAL-mixed\n(Track 1, 180s)\nMy submission", "FINAL-mixed", "#2ca02c"),
        ("FINAL_text_70u\n(Track 2, 180s)", "FINAL", "#1f77b4"),
        ("Reference 2r mixed\n(workshop baseline)", None, "#888888"),
        ("Reference 4r mixed\n(workshop baseline)", None, "#888888"),
    ]

    # Reference baseline scores computed from runbook table values.
    #
    # 2-replica mixed:
    #   TTFT = 1169 ms
    #   ITL = 28.7 ms
    #   throughput = 2243 chunks/s
    #   users = 100
    #   GPUs = 2
    ref_2r = 2243 * 100 * 1.0 / (1.169 * 0.0287 * 2)

    # 4-replica mixed:
    #   TTFT = 898 ms
    #   ITL = 38.1 ms
    #   throughput = 2756 chunks/s
    #   users = 120
    #   GPUs = 4
    ref_4r = 2756 * 120 * 1.0 / (0.898 * 0.0381 * 4)

    labels = []
    scores = []
    colors = []

    # Build the plotted values.
    for display_label, key, color in items:
        labels.append(display_label)
        colors.append(color)

        # For real runs, compute score from JSON.
        if key and key in runs:
            scores.append(score(runs[key]) / 1e6)

        # For reference rows, use the manually computed references.
        elif "2r" in display_label:
            scores.append(ref_2r / 1e6)

        elif "4r" in display_label:
            scores.append(ref_4r / 1e6)

        # Fallback for missing data.
        else:
            scores.append(0)

    # Create bar chart.
    fig, ax = plt.subplots(figsize=(11, 6))
    bars = ax.bar(
        labels,
        scores,
        color=colors,
        edgecolor="black",
        linewidth=0.6,
    )

    ax.set_ylabel("Score (millions)", fontsize=12)
    ax.set_title(
        "Final Submission Comparison — Both Tracks Crush the Reference Baselines",
        fontsize=13,
        pad=14,
    )
    ax.grid(True, axis="y", alpha=0.25, ls="--")

    # Add value labels above bars.
    for bar, val in zip(bars, scores):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            val + max(scores) * 0.015,
            f"{val:.1f}M",
            ha="center",
            fontsize=11,
            fontweight="bold",
        )

    # Annotate the ratio of FINAL-mixed to the 2-replica reference.
    final_mixed = score(runs["FINAL-mixed"]) / 1e6

    ax.annotate(
        f"{final_mixed / (ref_2r / 1e6):.1f}× the 2-replica\nreference baseline",
        xy=(0, final_mixed),
        xytext=(0.4, final_mixed * 0.85),
        fontsize=10,
        color="darkgreen",
        fontweight="bold",
        arrowprops={
            "arrowstyle": "->",
            "color": "darkgreen",
            "lw": 1.4,
        },
    )

    fig.tight_layout()
    out = PLOTS / "04_final_summary.png"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)

    print(f"Wrote {out}")


