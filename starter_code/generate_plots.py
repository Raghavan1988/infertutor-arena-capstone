def plot_compiled_vs_eager(runs):
    """
    Compare eager mode against compiled mode for both tracks.

    Text track comparison:
        baseline-text at 50 users
        compiled-70u at 70 users

    Multimodal track comparison:
        m-50u at 50 users
        m-compiled-50u at 50 users

    The chart groups TTFT, ITL, and throughput side-by-side.
    """

    # Create two side-by-side plots:
    #   left: text track
    #   right: multimodal track
    fig, axes = plt.subplots(1, 2, figsize=(13, 5.8))

    # Width of each grouped bar.
    w = 0.25

    # -----------------------------------------------------------------------
    # Left plot: text track
    # -----------------------------------------------------------------------

    ax = axes[0]

    configs = ["Eager\n(50u)", "Compiled\n(70u)"]

    # Pull TTFT p95 in milliseconds.
    ttft = [
        runs["baseline-text"]["results"]["ttft_p95_ms"],
        runs["compiled-70u"]["results"]["ttft_p95_ms"],
    ]

    # Pull ITL p95 in milliseconds.
    itl = [
        runs["baseline-text"]["results"]["itl_p95_ms"],
        runs["compiled-70u"]["results"]["itl_p95_ms"],
    ]

    # Pull aggregate throughput in chunks per second.
    thr = [
        runs["baseline-text"]["results"]["aggregate_stream_chunks_per_s"],
        runs["compiled-70u"]["results"]["aggregate_stream_chunks_per_s"],
    ]

    # X locations for the two grouped categories.
    x = np.arange(2)

    # Plot TTFT directly.
    ax.bar(
        x - w,
        ttft,
        w,
        label="TTFT p95 (ms)",
        color="#d62728",
    )

    # Multiply ITL by 30 so it is visually comparable on the same axis.
    # This is a visualization scaling trick, not a metric change.
    ax.bar(
        x,
        [value * 30 for value in itl],
        w,
        label="ITL p95 × 30 (ms)",
        color="#ff7f0e",
    )

    # Plot throughput directly.
    ax.bar(
        x + w,
        thr,
        w,
        label="Throughput (chunks/s)",
        color="#2ca02c",
    )

    ax.set_xticks(x)
    ax.set_xticklabels(configs)

    ax.set_title(
        "Text Track\n"
        "Compiled mode at 70u beats eager at 50u on every metric",
        fontsize=12,
    )

    ax.grid(True, axis="y", alpha=0.25, ls="--")
    ax.legend(loc="upper left", fontsize=9.5)

    # -----------------------------------------------------------------------
    # Right plot: multimodal track
    # -----------------------------------------------------------------------

    ax = axes[1]

    configs = ["Eager\n(50u)", "Compiled\n(50u)"]

    ttft = [
        runs["m-50u"]["results"]["ttft_p95_ms"],
        runs["m-compiled-50u"]["results"]["ttft_p95_ms"],
    ]

    itl = [
        runs["m-50u"]["results"]["itl_p95_ms"],
        runs["m-compiled-50u"]["results"]["itl_p95_ms"],
    ]

    thr = [
        runs["m-50u"]["results"]["aggregate_stream_chunks_per_s"],
        runs["m-compiled-50u"]["results"]["aggregate_stream_chunks_per_s"],
    ]

    x = np.arange(2)

    ax.bar(
        x - w,
        ttft,
        w,
        label="TTFT p95 (ms)",
        color="#d62728",
    )

    ax.bar(
        x,
        [value * 30 for value in itl],
        w,
        label="ITL p95 × 30 (ms)",
        color="#ff7f0e",
    )

    ax.bar(
        x + w,
        thr,
        w,
        label="Throughput (chunks/s)",
        color="#2ca02c",
    )

    ax.set_xticks(x)
    ax.set_xticklabels(configs)

    ax.set_title(
        "Multimodal Track\n"
        "Compiled mode wins here too — contrary to runbook warning!",
        fontsize=12,
    )

    ax.grid(True, axis="y", alpha=0.25, ls="--")
    ax.legend(loc="upper left", fontsize=9.5)

    # Overall title for the two-panel figure.
    fig.suptitle(
        "Compiled Mode (`--no-fast-boot`) is the Dominant Knob in BOTH Tracks",
        fontsize=14,
        y=1.02,
    )

    fig.tight_layout()
    out = PLOTS / "03_compiled_vs_eager.png"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)

    print(f"Wrote {out}")


