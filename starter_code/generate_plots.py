def plot_score_decomposition(runs):
    """
    Render a text-only plot explaining the score calculation for FINAL-mixed.

    This is useful for reports because it makes the formula auditable and
    shows how throughput, users, latency, error rate, and GPU count combine.

    Note:
        This function intentionally preserves the original script's behavior.

        The shared score() function applies error_rate once through goodput:

            goodput = throughput * (1 - error_rate)

        This decomposition then multiplies by (1 - error_rate) again in the
        numerator. That means error_rate is applied twice here.

        If the goal is to exactly match score(), remove the second
        (1 - error_rate) factor from num.
    """

    # Extract final mixed result payload.
    final = runs["FINAL-mixed"]["results"]

    # Extract final mixed config payload.
    c = runs["FINAL-mixed"]["config"]

    # Compute error-adjusted throughput.
    goodput = final["aggregate_stream_chunks_per_s"] * (1 - final["error_rate"])

    # Convert latency metrics from milliseconds to seconds.
    ttft_s = final["ttft_p95_ms"] / 1000
    itl_s = final["itl_p95_ms"] / 1000

    # Preserve original decomposition behavior.
    # Important:
    #   This applies (1 - error_rate) a second time.
    num = goodput * c["users"] * (1 - final["error_rate"])

    # Denominator penalizes latency and GPU count.
    den = ttft_s * itl_s * c["total_gpus"]

    # Final decomposed score.
    final_score = num / den

    # Create a blank figure.
    fig, ax = plt.subplots(figsize=(11, 5))

    # Hide axes because this is a formula card, not a data plot.
    ax.axis("off")

    # Build the text block shown in the figure.
    text = (
        "FINAL-mixed Score Decomposition (Track 1 Submission)\n"
        "─────────────────────────────────────────────────────\n\n"
        "   Score = goodput × users × (1 − error_rate)\n"
        "           ────────────────────────────────────\n"
        "           p95_TTFT_s × p95_ITL_s × GPU_count\n\n"
        f"   Numerator   = {goodput:>7.0f}    × {c['users']:>3}  × "
        f"{(1 - final['error_rate']):.4f}\n"
        f"                = {num:>12,.0f}\n\n"
        f"   Denominator = {ttft_s:>7.4f}s × {itl_s:>7.5f}s × "
        f"{c['total_gpus']}\n"
        f"                = {den:>12.6f}\n\n"
        f"   SCORE       = {final_score:>12,.0f}\n"
        f"               = {final_score / 1e6:.2f} M"
    )

    # Draw the text block in monospace so columns align.
    ax.text(
        0.05,
        0.5,
        text,
        fontfamily="monospace",
        fontsize=12,
        verticalalignment="center",
        transform=ax.transAxes,
    )

    fig.tight_layout()
    out = PLOTS / "06_score_decomposition.png"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)

    print(f"Wrote {out}")


