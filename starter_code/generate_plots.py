def plot_text_knee(runs):
    """
    Plot text-track concurrency sweep.

    Goal:
        Show how p95 TTFT and throughput change as concurrent users increase.

    Interpretation:
        The "knee" is the point where adding users starts causing latency to
        rise sharply. In this benchmark, the knee is visually highlighted
        around 60 to 100 users.
    """

    # Ordered list of labels and their intended displayed user counts.
    # Some labels are manually selected because the report wants a specific
    # clean run for each user count.
    points = [
        ("smoke", 5),
        ("baseline-text", 50),
        ("sweep-60u", 60),
        ("sweep-70u", 70),
        ("sweep-80u", 80),
        ("sweep-90u", 90),
        ("sweep-100u", 100),
        ("sweep-300u", 300),
        ("sweep-400u", 400),
        ("sweep-500u", 500),
        ("sweep-600u", 600),
    ]

    # X-axis values.
    users = []

    # Left Y-axis values.
    ttft = []

    # Right Y-axis values.
    throughput = []

    # Build the plotted series.
    for label, user_count in points:
        # Find the run for this label.
        # Because runs is keyed by label, this usually returns either 0 or 1 item.
        candidates = [run for key, run in runs.items() if key == label]

        # Skip labels that are not present in the loaded results.
        if not candidates:
            continue

        # Preserve the original script behavior.
        # If candidates ever contains more than one item, choose the first one.
        chosen = candidates[0]

        # Append the values that will be plotted.
        users.append(user_count)
        ttft.append(chosen["results"]["ttft_p95_ms"])
        throughput.append(chosen["results"]["aggregate_stream_chunks_per_s"])

    # Create a figure and the first axis.
    fig, ax1 = plt.subplots(figsize=(11, 6.5))

    # Create a second Y-axis sharing the same X-axis.
    # This lets us plot latency and throughput on different scales.
    ax2 = ax1.twinx()

    # Explicit colors make the two axes easier to visually separate.
    color_ttft = "#d62728"
    color_thr = "#2ca02c"

    # Plot TTFT on the left Y-axis.
    ax1.plot(
        users,
        ttft,
        "o-",
        color=color_ttft,
        lw=2.4,
        ms=9,
        label="TTFT p95 (ms)",
        zorder=3,
    )

    # Plot throughput on the right Y-axis.
    ax2.plot(
        users,
        throughput,
        "s--",
        color=color_thr,
        lw=2,
        ms=8,
        label="Throughput (chunks/s)",
        zorder=2,
    )

    # Use log scale because TTFT can jump by orders of magnitude.
    ax1.set_yscale("log")

    # Axis labels.
    ax1.set_xlabel("Concurrent users", fontsize=12)
    ax1.set_ylabel("TTFT p95 (ms, log scale)", color=color_ttft, fontsize=12)
    ax2.set_ylabel("Throughput (chunks/s)", color=color_thr, fontsize=12)

    # Match tick label colors with each axis line color.
    ax1.tick_params(axis="y", labelcolor=color_ttft)
    ax2.tick_params(axis="y", labelcolor=color_thr)

    # Highlight the knee zone where latency starts to bend upward.
    ax1.axvspan(
        60,
        100,
        alpha=0.18,
        color="orange",
        label="The knee (60-100u)",
    )

    # Annotate the baseline point if it exists.
    if 50 in users:
        i = users.index(50)
        ax1.annotate(
            "  baseline\n  50u, 726ms",
            (users[i], ttft[i]),
            textcoords="offset points",
            xytext=(10, -22),
            fontsize=9.5,
            color=color_ttft,
        )

    # Annotate the 100-user jump if it exists.
    if 100 in users:
        i = users.index(100)
        ax1.annotate(
            "100u\n10× TTFT jump\nvs 60u",
            (users[i], ttft[i]),
            textcoords="offset points",
            xytext=(15, -5),
            fontsize=9.5,
            color="darkred",
            fontweight="bold",
        )

    # Main title.
    ax1.set_title(
        "Text-Track Knee Map — Default Knobs, 1 GPU\n"
        "TTFT p95 stays flat through 60u, then explodes by 100u",
        fontsize=13,
        pad=14,
    )

    # Add a light grid for readability.
    ax1.grid(True, alpha=0.25, ls="--")

    # Force stable X-axis ticks so the plot is easy to compare across runs.
    ax1.set_xticks([0, 50, 100, 200, 300, 400, 500, 600])

    # Collect legends from both axes.
    lines1, labels1 = ax1.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()

    # Draw one combined legend.
    ax1.legend(
        lines1 + lines2,
        labels1 + labels2,
        loc="center right",
        framealpha=0.95,
    )

    # Prevent labels and titles from being clipped.
    fig.tight_layout()

    # Save output.
    out = PLOTS / "01_text_knee.png"
    fig.savefig(out, bbox_inches="tight")

    # Close figure to avoid memory buildup when generating many plots.
    plt.close(fig)

    print(f"Wrote {out}")


