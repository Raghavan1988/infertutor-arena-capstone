def load_all():
    """
    Load every JSON benchmark result from results_infertutor/.

    Returns:
        dict:
            Mapping from run label to the loaded benchmark payload.

            Example:
                {
                    "baseline-text": {
                        "file": "baseline-text.json",
                        "config": {...},
                        "results": {...}
                    }
                }

    Important behavior:
        The dictionary key is data["config"]["label"].

        If multiple JSON files contain the same label, the later file in the
        sorted file order will overwrite the earlier one. This matches the
        original behavior, but it means labels should ideally be unique.
    """

    # Store all loaded runs here.
    runs = {}

    # Iterate through all JSON files in deterministic filename order.
    for f in sorted(RESULTS.glob("*.json")):
        # Read the file as text and parse it as JSON.
        data = json.loads(f.read_text())

        # Use the human-readable config label as the dictionary key.
        label = data["config"]["label"]

        # Store the file name alongside the original JSON payload.
        # The **data spread keeps all original top-level fields.
        runs[label] = {
            "file": f.name,
            **data,
        }

    return runs


# ---------------------------------------------------------------------------
# Scoring function
# ---------------------------------------------------------------------------

def score(data):
    """
    Compute the custom engineering-report score for a single benchmark run.

    Score intuition:
        Bigger is better.

        The score rewards:
            - higher streaming throughput
            - more concurrent users

        The score penalizes:
            - higher error rate
            - higher p95 time-to-first-token
            - higher p95 inter-token latency
            - more GPUs

    Formula:
        goodput = throughput * (1 - error_rate)

        score = goodput * users / (ttft_seconds * itl_seconds * total_gpus)

    Args:
        data:
            One loaded benchmark run containing "config" and "results".

    Returns:
        float:
            Computed score.
    """

    # Pull config and results into shorter local variables.
    c = data["config"]
    r = data["results"]

    # Goodput is throughput adjusted downward by error rate.
    # max(0, ...) prevents negative goodput if error_rate somehow exceeds 1.
    goodput = r["aggregate_stream_chunks_per_s"] * max(0, 1 - r["error_rate"])

    # Convert TTFT from milliseconds to seconds.
    # Use a tiny floor value to avoid division by zero.
    ttft = max(r["ttft_p95_ms"] / 1000, 0.001)

    # Convert ITL from milliseconds to seconds.
    # Use a tiny floor value to avoid division by zero.
    itl = max(r["itl_p95_ms"] / 1000, 0.001)

    # Protect against division by zero if total_gpus is missing or set to 0.
    gpu_count = max(c["total_gpus"], 1)

    # Final score.
    return goodput * c["users"] / (ttft * itl * gpu_count)


