def plot_knob_ablations(runs):
    """
    Plot text-track knob ablation scores.

    Goal:
        Compare several configuration changes against the baseline.

    Interpretation:
        The plot shows that compiled mode is the only winning knob among the
        listed options, while widening-related knobs underperform.
    """

    # Each tuple contains:
    #   display label for plot,
    #   key used to look up the run,
    #   bar color.
    items = [
        ("baseline\n(50u, default)", "baseline-text", "#1f77b4"),
        ("--no-prefix-cache\n(70u)", "noprefix-70u", "#d62728"),
        ("--no-chunked-prefill\n(70u)", "nochunked-70u", "#ff7f0e"),
        ("--max-seqs 64\n(70u)", "seqs64-70u", "#d62728"),
        ("--max-batch-tokens 8192\n(70u)", "batch8k-70u", "#d62728"),
        ("--replicas 2 (eager)\n(140u, 2 GPU)", "scale-r2-140u", "#d62728"),
        ("--no-fast-boot\n(compiled, 70u)", "compiled-70u", "#2ca02c"),
    ]

    labels = []
    scores = []
    colors = []

    # Convert each run into a score in millions.
    for display_label, key, color in items:
        # Only include runs that are present in the result set.
        if key in runs:
            labels.append(display_label)
            scores.append(score(runs[key]) / 1e6)
            colors.append(color)

    # Create bar chart.
    fig, ax = plt.subplots(figsize=(13, 6.5))
    bars = ax.bar(
        labels,
        scores,
        color=colors,
        edgecolor="black",
        linewidth=0.6,
    )

    # Use log scale because ablation scores can vary sharply.
    ax.set_yscale("log")

    # Axis label and title.
    ax.set_ylabel("Score (millions, log scale)", fontsize=12)
    ax.set_title(
        "Text-Track Knob A/Bs at 70 Users — Compiled Mode is the Only Win\n"
        "Bigger is better. All 'widening' knobs scored worse than baseline.",
        fontsize=13,
        pad=14,
    )

    # Horizontal grid only.
    ax.grid(True, axis="y", alpha=0.25, ls="--")

    # Label every bar with its numeric score.
    for bar, val in zip(bars, scores):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            val * 1.12,
            f"{val:.2f}M",
            ha="center",
            fontsize=10.5,
            fontweight="bold",
        )

    # Keep labels horizontal and readable.
    plt.setp(ax.get_xticklabels(), rotation=0, fontsize=9.5)

    # Final layout and save.
    fig.tight_layout()
    out = PLOTS / "02_text_knob_ablations.png"
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)

    print(f"Wrote {out}")


